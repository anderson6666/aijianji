// 按分类分组
export const effectDefinitionsByCategory = effectDefinitions.reduce(
  (acc, def) => {
    if (!acc[def.category]) {
      acc[def.category] = []
    }
    acc[def.category].push(def)
    return acc;
  },
  {} as Record<string, EffectDefinition[]>
)

// 根据ID查找效果定义
export const getEffectDefinition = (id: string): EffectDefinition | undefined => {
  return effectDefinitions.find((def) => def.id === id)
}
